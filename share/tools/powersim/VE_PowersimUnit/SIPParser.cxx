
// --- VE_PowersimUnit Includes --- //
#include "StdAtl.h"
#include "SIPParser.h"

// --- CPSI Includes --- //
#include <CPSI.h>

////////////////////////////////////////////////////////////////////////////////
SIPParser::SIPParser()
    :
    m_powerimProject( new cpsi::Project() )
{
    ;
}
////////////////////////////////////////////////////////////////////////////////
SIPParser::~SIPParser()
{
    ;
}
////////////////////////////////////////////////////////////////////////////////
void SIPParser::OpenSimulation( const std::string& fileName )
{
    m_powerimProject->Open(
        ( m_workingDir + fileName + ".sip" ).c_str(), "", "" );
}
////////////////////////////////////////////////////////////////////////////////
void SIPParser::SetWorkingDir( const std::string& workingDir )
{
    m_workingDir = workingDir;
}
